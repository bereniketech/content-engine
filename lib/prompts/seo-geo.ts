    '  }',
    '}',
    '',
    'Requirements:',
    '- seo.title: include primary keyword naturally and stay within 50-60 chars.',
    '- seo.metaDescription: include value proposition and stay within 150-160 chars.',
    '- seo.slug: lowercase, hyphenated, concise.',
    '- seo.secondaryKeywords: exactly 5 items.',
    '- seo.headingStructure.h2 and h3: arrays of relevant headings based on article structure.',
    '- seo.faqSchema: 3 high-intent questions answered from article facts.',
    '- seo.seoScore: integer from 0 to 100.',
    '- geo.citationOptimization: rewrite high-value statements to be citation-friendly.',
    '- geo.entityMarking: notable entities with short descriptions.',
    '- geo.conciseAnswers: direct Q&A suitable for AI overviews.',
